package mappers.Artist;

/**
 * Artist class for mapper
 */
public class Artist {

    /* Artist ID */
    private String id;

    /* Artist name */
    private String name;

    /**
     * Gets the name of the artist
     * @return name
     */
    public String getName() {
        return name;
    }

    /**
     * Gets the name of the ID
     * @return id;
     */
    public String getId() {
        return id;
    }
}
